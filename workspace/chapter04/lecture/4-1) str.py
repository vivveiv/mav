# str 클래스 변수(var) 객체(object)
#순서 자료구조를 갖는 열거형 객체

# 기본형식 ######!!!!!!!!!!!!!!!!!!!!!!!
str_var = str(object='string')
print(str_var)
print(type(str_var))
print(str_var[0])
print(str_var[-1])

# 간편형식
str_var2 = 'string'
print(str_var2)
print(type(str_var2))
print(str_var2[0])
print(str_var2[-1])


